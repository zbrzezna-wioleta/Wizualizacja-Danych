def n_ty_wyraz_a1 (a1, n, r):
    return a1 + (n-1) * r

def n_ty_wyraz_ak (ak, n, k, r):
    return ak + (n-k) * r

def n_ty_wyraz_suma (Sn, Sn1):
    return Sn - Sn1

def suma_p_i_n (a1, an, n):
    return (a1 + an)/2 * n

def suma_p (a1, n, r):
    return (2 * a1 + (n - 1) * r)/2 * n